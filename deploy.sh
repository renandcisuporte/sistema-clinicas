#!/bin/bash
set -e

echo "########################################################################################"
echo "#################################### BUILD / START #####################################"
echo "########################################################################################"
docker compose up -d --build --remove-orphans --force-recreate

echo "########################################################################################"
echo "#################################### MIGRATE DB ########################################"
echo "########################################################################################"
docker compose exec -T app sh -lc '
  npm install &&
  npx prisma migrate deploy
  npm prune --omit=dev
'

echo "########################################################################################"
echo "#################################### STATUS ############################################"
echo "########################################################################################"
docker compose ps